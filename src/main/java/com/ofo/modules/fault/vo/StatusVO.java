package com.ofo.modules.fault.vo;

public class StatusVO {
	
	private Integer id;
	
	private String ofoCode;
	
	private String ofoStatus;
	
	private String location;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getOfoCode() {
		return ofoCode;
	}

	public void setOfoCode(String ofoCode) {
		this.ofoCode = ofoCode;
	}

	public String getOfoStatus() {
		return ofoStatus;
	}

	public void setOfoStatus(String ofoStatus) {
		this.ofoStatus = ofoStatus;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}
	
	

}
